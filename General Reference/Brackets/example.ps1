Powershell allows the following brackets:
- Parenthesis () - Used for closing multiple statements, loops, passing of parameters, array value assignments
- Braces brackets {} - Execute block of statement, enclose code
- Square brackets [] - Represent array items e.g. $array[0] = 1st value of array
- <angle> bracket - Typically used for HTML docs e.g.:

